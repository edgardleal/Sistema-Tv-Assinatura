    public boolean verificaCampo(){
        boolean habilitado = true;
        if(jComboBoxSituacao.getSelectedIndex()==0){
            habilitado = false;
            lblSituacao.setForeground(Color.RED);
        }else{
           lblSituacao.setForeground(Color.black);
        }
        if(tMotivo.getText().length()==0){
            habilitado = false;
            lblMotivo.setForeground(Color.RED);
        }else{
            lblMotivo.setForeground(Color.black);
        }
        return habilitado;
    }
    
