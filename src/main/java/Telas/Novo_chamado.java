    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked

    if(verificaCampo()){
       String motivo =tMotivo.getText();
        String situacao = (String) jComboBoxSituacao.getSelectedItem();
        int idContrato = this.contrato.getIdContrato();
        int idPlano = this.contrato.getIdPlano();
        int idCliente = this.contrato.getIdCliente();

        Chamado c  = new Chamado(situacao, motivo, idCliente, idPlano, idContrato);
        if(Chamado.adicionaChamado(c)){
            Chamados.preencheTabela();
            dispose();
            new Operacao_sucesso().setVisible(true);
        }else{
            dispose();
            JOptionPane.showMessageDialog(null, "falhar ao incluir");
        }  
    }else{
        new Campos_preenchidos().setVisible(true);
    }
        

    }//GEN-LAST:event_jPanel1MouseClicked

