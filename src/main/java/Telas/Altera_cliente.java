    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked
        if(verificaCampos()){
            String nome = tNomeCliente.getText();
            String CPF = tCPFCliente.getText();
            String email = tEmailCliente.getText();
            String telefone = tTelefoneCliente.getText();
            int id = (int) spinnerId.getValue();
            
            Cliente cliente = new Cliente(nome, email, CPF, telefone, id);
            if(Cliente.alteraCliente(cliente)){
                new Operacao_sucesso().setVisible(true);
                dispose();
                Clientes.preencheTabela();
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao atualizar");
            }
        }else{
            new Campos_preenchidos().setVisible(true);
        }
    }//GEN-LAST:event_jPanel1MouseClicked

