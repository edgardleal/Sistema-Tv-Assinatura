    private void btnPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisarMouseClicked
        if(jCPFCliente.getText().length() == 0){
            new Erro_cpf().setVisible(true);
            lblCliente.setVisible(false);
            lblNomeCliente.setVisible(false);
            jCPFCliente.setText("");
            preencheTabela(new ArrayList<>());
        }else {
            String CPF = jCPFCliente.getText();
            Cliente c = Cliente.buscaUmCliente(CPF);
            lblCliente.setVisible(true);
            lblNomeCliente.setText(c.getNome());
            lblNomeCliente.setVisible(true);
            ArrayList<Contrato> contratos = Contrato.devolveContratos(c.getIdCliente());
            preencheTabela(contratos);
            jCPFCliente.setText("");
        }
    }//GEN-LAST:event_btnPesquisarMouseClicked

