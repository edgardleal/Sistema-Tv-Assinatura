    public static void preencheTabela(ArrayList<Contrato> contratos){
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        if(modelo.getRowCount()>0)
            modelo.setRowCount(0);
        
        for(Contrato contrato: contratos){
            String acesso = "";
            if(contrato.getAcesso()==1){
                acesso = "Habilitado";
            }else{
                acesso = "Desabilitado";
            }
            modelo.addRow(new Object[]{
                contrato.getIdContrato(),
                contrato.getNomePlano(),
                contrato.getQuantReceptores(),
                contrato.getEndCobranca(),
                acesso,
                contrato.getIdPlano()
            });
        }
    }
    
    
