    private void btnRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoverMouseClicked
        if( Tabela.getSelectedRow() != -1){
            int idCliente = (int) Tabela.getValueAt(Tabela.getSelectedRow(), 4);
            ArrayList<Contrato> contratos = Contrato.devolveContratos(idCliente);
            if(contratos.size()==0){
                new perguntaExcluirCliente().setVisible(true);
            }else{
                new Erro_exclusão_cliente().setVisible(true);
            }
        }else{
            new Error_exclusão_canal().setVisible(true);
        }
    }//GEN-LAST:event_btnRemoverMouseClicked

