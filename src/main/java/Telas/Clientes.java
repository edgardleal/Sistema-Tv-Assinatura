    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        if( Tabela.getSelectedRow() != -1){
            DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
            String nome = (String) Tabela.getValueAt(Tabela.getSelectedRow(), 0);
            String cpf = (String) Tabela.getValueAt(Tabela.getSelectedRow(), 1);
            String telefone = (String) Tabela.getValueAt(Tabela.getSelectedRow(), 2);
            String email = (String) Tabela.getValueAt(Tabela.getSelectedRow(), 3);
            int id = (Integer) Tabela.getValueAt(Tabela.getSelectedRow(), 4);
            
           Altera_cliente cliente = new Altera_cliente();
           cliente.setaCampos(nome, cpf, telefone, email, id);
           cliente.setVisible(true);
            
        
        }else{
            new Error_atualização_canal().setVisible(true);
        }    
    }//GEN-LAST:event_btnEditarMouseClicked

