    public static void preencheTabela(){
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        
        if(modelo.getRowCount() > 0){
             modelo.setNumRows(0);
        }
        
        ArrayList<Cliente> clientes = new ArrayList<>();
        clientes = Cliente.buscarClientes();
        
        for(Cliente cliente: clientes){
            modelo.addRow(new Object[]{
                cliente.getNome(),
                cliente.getCpf(),
                cliente.getTelefone(),
                cliente.getEmail(),
                cliente.getIdCliente()
            });
        }
    }
    public static void preencheTabela(ArrayList<Cliente> clientes){
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        
        if(modelo.getRowCount() > 0){
             modelo.setNumRows(0);
        }
        
        
        for(Cliente cliente: clientes){
            modelo.addRow(new Object[]{
                cliente.getNome(),
                cliente.getCpf(),
                cliente.getTelefone(),
                cliente.getEmail(),
                cliente.getIdCliente()
            });
        }
    }

