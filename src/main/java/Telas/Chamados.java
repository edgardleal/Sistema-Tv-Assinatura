    public static void preencheTabela(){
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        
        if(modelo.getRowCount() > 0){
             modelo.setNumRows(0);
        }
        
        ArrayList<Chamado> chamados = Chamado.procuraChamados(idContrato);
        DateTimeFormatter formatador = DateTimeFormatter.ofPattern("dd/LLLL/yyyy");
        for(Chamado chamado: chamados){
            
            String dataFormatadaPagamento=null;
            String dataFormatada = chamado.getData().format(formatador);
            String dataFormatadaAtualizacao = chamado.getUltima_atualizacao().format(formatador);
   
            modelo.addRow(new Object[]{
               chamado.getIdChamado(),
               chamado.getMotivo(),
               chamado.getSituacao(),
               dataFormatada,
               dataFormatadaAtualizacao
            });
        }
    }
