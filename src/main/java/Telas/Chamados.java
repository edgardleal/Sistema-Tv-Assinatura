    private void btnPesquisar3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisar3MouseClicked
        if(jNumeroContrato.getText().length()==0){
            preencheTabela();
        }else{
            int numeroContrato = Integer.parseInt(jNumeroContrato.getText());
            ArrayList<Chamado> chamados = new ArrayList<>();
            chamados = Chamado.procuraChamados(idContrato, numeroContrato);
            preencheTabela(chamados);
            jNumeroContrato.setText("");
            
        }
    }//GEN-LAST:event_btnPesquisar3MouseClicked

