    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked
        if(verificaCampos()){
            String nome = tNomeCanal.getText();
            int valorNumero =  Integer.parseInt(tNumero.getText());
            System.out.println(valorNumero);
            float PrecoBase = parseFloat(tPrecoBase.getText());
            int valorClassEtaria = (int) classEtaria.getValue();
            int idCategoria = categoria.getSelectedIndex();
            Canal canal = new Canal(nome, valorNumero, valorClassEtaria, PrecoBase, idCategoria);
            CanalDAO dao = new CanalDAO();
            
            if(dao.atualizarCanal(canal)){
                new Operacao_sucesso().setVisible(true);
            }else{
               JOptionPane.showMessageDialog(null, "Erro ao alterar!");
            };
            
            Canais.preencherTabela();
        }else{
            new Campos_preenchidos().setVisible(true);
        }
           
    }//GEN-LAST:event_jPanel1MouseClicked

    
