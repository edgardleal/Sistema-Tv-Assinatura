    public boolean verificaCampos(){
        boolean habilitado = true;
        if(tNomeCanal.getText().length()==0){
            habilitado = false;
            lblNomeCanal.setForeground(Color.red);
        }else{
            lblNomeCanal.setForeground(Color.black);
        }
        if(categoria.getSelectedIndex()==0){
            habilitado = false;
            lblCategoria.setForeground(Color.red);
        }else{
            lblCategoria.setForeground(Color.black);
        }
        if(tPrecoBase.getText().length()==0){
            habilitado = false;
            lblPrecoBase.setForeground(Color.red);
        }else{
            lblPrecoBase.setForeground(Color.black);
        }
        return habilitado;
    }

