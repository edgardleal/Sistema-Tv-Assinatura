    public boolean verificaCampos(){
        boolean verificador = true;
        if(tNomeCliente.getText().length()==0){
            setColorRed(lblNome);
            verificador = false;
        }else{
            setColorBlack(lblNome);
        }
        
        if(tCPFCliente.getText().length()==0){
            setColorRed(lblCPF);
            verificador = false;
        }else{
            setColorBlack(lblCPF);
        }
        
        if(tTelefoneCliente.getText().length()==0){
            setColorRed(lblTelefone);
            verificador = false;
        }else{
            setColorBlack(lblTelefone);
        }
        
        if(tEmailCliente.getText().length()==0){
            setColorRed(lblEmail);
            verificador = false;
        }else{
            setColorBlack(lblEmail);
        }
        
       return verificador;
    }
    
